# @lc code=start
# Definition for singly-linked list.
# class ListNode:
#     def __init__(self, val=0, next=None):
#         self.val = val
#         self.next = next
class Solution:
    def reverseList(self, head: ListNode) -> ListNode:
        p = None
        cur = None
        while head:
            cur = head.next
            head.next = p
            p = head
            head = cur
        return p
        # @lc code=end
